export const selectEmote = (emoteID) => {

  if (emoteID === 1) {
    return "🙂 "
  }
  else if (emoteID === 2) {
    return "😃"
  }
  else if (emoteID === 3) {
    return "🙃 "
  }
  else if (emoteID === 4) {
    return "🥰 "
  }
  else if (emoteID === 5) {
    return "😍 "
  }
  else if (emoteID === 6) {
    return "😐 "
  }
  else if (emoteID === 7) {
    return " 😔"
  }
  else if (emoteID === 8) {
    return "😕 "
  }
  else if (emoteID === 9) {
    return "😭 "
  }
  else if (emoteID === 10) {
    return "😢 "
  }
  else if (emoteID === 11) {
    return "😩 "
  }
  else if (emoteID === 12) {
    return "😠 "
  }
  else if (emoteID === 0) {
    return "😐"
  }
}
